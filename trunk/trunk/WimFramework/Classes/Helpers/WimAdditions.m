#import "WimAdditions.h"

@implementation NSString (URLEncode)
-(NSString *) urlencode
{
    NSArray *escapeChars = [NSArray arrayWithObjects:@";" , @"/" , @"?" , @":" ,
							@"@" , @"&" , @"=" , @"+" ,
							@"$" , @"," , @"[" , @"]",
							@"#", @"!", @"'", @"(", 
							@")", @"*", @" ", nil];
	
    NSArray *replaceChars = [NSArray arrayWithObjects:@"%3B" , @"%2F" , @"%3F" ,
							 @"%3A" , @"%40" , @"%26" ,
							 @"%3D" , @"%2B" , @"%24" ,
							 @"%2C" , @"%5B" , @"%5D", 
							 @"%23", @"%21", @"%27",
							 @"%28", @"%29", @"%2A", @"%20", nil];
	
    int len = [escapeChars count];
	
    NSMutableString *temp = [self mutableCopy];
	
    int i;
    for(i = 0; i < len; i++)
    {
		
        [temp replaceOccurrencesOfString: [escapeChars objectAtIndex:i]
							  withString:[replaceChars objectAtIndex:i]
								 options:NSLiteralSearch
								   range:NSMakeRange(0, [temp length])];
    }
	
    return temp;
}
@end

@implementation NSMutableString (URLEncode)
- (NSMutableString *)appendValue:(NSString *)value forName:(NSString *)name
{
	if ([self length])
	{
		[self appendString:@"&"];
	}
	
	[self appendFormat:@"%@=%@", name, [value stringByAddingPercentEscapesUsingEncoding:NSUTF8StringEncoding]];
	return self;
}

@end
